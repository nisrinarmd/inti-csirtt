        <h1 style="margin-left:150px; color:white;"><br>TENTANG INTI-CSIRT</h1><br><br>
        <div class="parag"><br>
            <p>Dokumen ini berisi deskripsi INTI-CSIRT berdasarkan RFC 2350, yaitu <br>informasi dasar mengenai INTI-CSIRT, menjelaskan tanggung <br>jawab, layanan yang diberikan, dan cara untuk menghubungi INTI-<br>CSIRT.</p>
            <p><b>
                    <h2>1.Visi</h2>
                </b> Visi INTI-CSIRT adalah menjadi komponen utama untuk <br>tercapainya Visi Perusahaan dengan meningkatkan <br>ketahanan siber di sektor industri telekomunikasi.</p>
            <p><b>
                    <h2>2.Misi</h2>
                </b>Perwujudan visi sebagaimana dituangkan di atas akan <br>dicapai melalui upaya- upaya yang terkandung dalam <br>misi INTI-CSIRT, yaitu :
                <br>1. Mengkoordinasikan dan mengkolaborasikan layanan <br>keamanan siber di lingkungan Perusahaan dan <br>pemangku kepentingan.
                <br>2. Membangun kemampuan dan kapasitas sumber daya <br>keamanan di sektor industri BUMN.
                <br>3. Membangun kerjasama dalam rangka <br>penanggulangan dan pemulihan insiden keamanan siber <br>di lingkungan sektor industri BUMN.
            </p>
            <p><b>
                    <h2>3.Konstituen</h2>
                </b>Konstituen INTI-CSIRT meliputi seluruh pengguna yang <br>menggunakan layanan Data Center Perusahaan <br>Perseroan (Persero) PT Industri Telekomunikasi Indonesia, <br>meliputi karyawan dan anak perusahaan, yaitu PT. Inti <br>Konten Indonesia (INTENS) dan PT. Inti Pindad Mitra Sejati <br>(IPMS).</p>
            <p><b>
                    <h2>4.Sponsorship dan/atau Afiliasi</h2>
                </b>Pendanaan INTI-CSIRT bersumber dari anggaran <br>pemeliharaan infrastrukutur Teknologi Informasi <br>Perusahaan Perseroan (Persero) PT Industri <br>Telekomunikasi Indonesia yang sudah dianggarkan setiap <br>tahunnya.</p>
            <p><b>
                    <h2>5.Otoritas</h2>
                </b><br> Tim INTI-CSIRT memiliki kewenangan antara lain:
                <br>1. Menentukan asesmen tingkat keamanan informasi pada <br>proses bisnis yang sedang atau yang akan berlangsung;
                <br>2. Melakukan asesmen tingkat keamanan sistem informasi <br>yang dibuat secara sendiri (in-house), atau disewa/dibeli <br>ke pihak ketiga;
                <br>3. Melakukan pengawasan serta intervensi aktif terhadap <br>operasional sistem informasi dalam rangka pemenuhan <br>ketahanan dan keandalan siber yang menunjang tujuan <br>bisnis;
                <br>4. Merencanakan, membuat dan mengoperasikan <br>rancang bangun mekanisme pertahanan berlapis <br>siber (cyber defense-in-depth);
                <br>5. Melaksanakan program kesadaran keamanan siber <br>bersama stakeholder terkait;
                <br>6. Memiliki otoritas penuh untuk melaksanakan koordinasi <br>dan intervensi internal dan eksternal, akses terhadap data <br>dan sistem dalam hal penanganan insiden siber Anti- <br>Malware.
            </p>
